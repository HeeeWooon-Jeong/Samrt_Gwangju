package doWhile;

public class Do_while문 {

	public static void main(String[] args) {
		
		//do - while문
		
		do {
			System.out.println("반드시 한번 실행!");
		} while(false);
		
		// while 문, do-while문
		//-->반복 횟수가 명확하지 않을 때 주로 사용
		//do-while문은 반드시 한번은 실행해야 하는 코드가 있을 때 사용!
		//-->검사조건이 false 여도 하번은 실행 된다!!
		
		
		
		
		
	}

}
